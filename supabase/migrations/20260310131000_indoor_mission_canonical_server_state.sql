create or replace function public.normalize_indoor_mission_pet_context_id(raw text)
returns text
language sql
immutable
as $$
  select coalesce(nullif(trim(raw), ''), '__none__');
$$;

create or replace function public.normalize_indoor_mission_day_key(raw text, now_ts timestamptz default now())
returns date
language sql
stable
as $$
  select coalesce(
    to_date(nullif(trim(raw), ''), 'YYYY-MM-DD'),
    (timezone('utc', now_ts))::date
  );
$$;

create or replace function public.indoor_mission_replacement_count(risk text)
returns integer
language sql
immutable
as $$
  select case public.normalize_indoor_weather_risk(risk)
    when 'caution' then 1
    when 'bad' then 2
    when 'severe' then 3
    else 0
  end;
$$;

create or replace function public.indoor_mission_reward_scale(risk text)
returns double precision
language sql
immutable
as $$
  select case public.normalize_indoor_weather_risk(risk)
    when 'caution' then 0.92
    when 'bad' then 0.88
    when 'severe' then 0.84
    else 1.0
  end;
$$;

create or replace function public.indoor_mission_age_band(age_years integer)
returns text
language sql
immutable
as $$
  select case
    when age_years is null then 'unknown'
    when age_years <= 1 then 'puppy'
    when age_years >= 10 then 'senior'
    else 'adult'
  end;
$$;

create or replace function public.indoor_mission_activity_level(recent_daily_minutes double precision)
returns text
language sql
immutable
as $$
  select case
    when coalesce(recent_daily_minutes, 0) < 20 then 'low'
    when coalesce(recent_daily_minutes, 0) > 65 then 'high'
    else 'moderate'
  end;
$$;

create or replace function public.indoor_mission_walk_frequency_band(average_weekly_walk_count double precision)
returns text
language sql
immutable
as $$
  select case
    when coalesce(average_weekly_walk_count, 0) < 3 then 'sparse'
    when coalesce(average_weekly_walk_count, 0) > 10 then 'frequent'
    else 'steady'
  end;
$$;

create or replace function public.indoor_mission_template_priority(category text, risk text)
returns integer
language sql
immutable
as $$
  select case public.normalize_indoor_weather_risk(risk)
    when 'caution' then case category
      when 'petCareCheck' then 0
      when 'recordCleanup' then 1
      when 'trainingCheck' then 2
      else 99
    end
    when 'bad' then case category
      when 'recordCleanup' then 0
      when 'petCareCheck' then 1
      when 'trainingCheck' then 2
      else 99
    end
    when 'severe' then case category
      when 'petCareCheck' then 0
      when 'trainingCheck' then 1
      when 'recordCleanup' then 2
      else 99
    end
    else 99
  end;
$$;

create or replace function public.indoor_mission_extension_message(state text)
returns text
language sql
immutable
as $$
  select case lower(coalesce(state, 'none'))
    when 'active' then '전일 미션 1개를 자동 연장했어요. 완료 시 보상은 기본의 70%만 지급돼요.'
    when 'consumed' then '연장 슬롯 사용 완료. 연장 미션은 시즌 점수/연속 보상에서 제외돼요.'
    when 'expired' then '전일 연장 미션이 미완료로 소멸되어 오늘은 자동 연장이 제공되지 않아요.'
    when 'cooldown' then '연장 슬롯은 연속 2일 이상 자동 적용되지 않아요. 오늘은 쿨다운 상태예요.'
    else null
  end;
$$;


create table if not exists public.indoor_mission_templates (
  id text primary key,
  category text not null,
  title text not null,
  description text not null,
  minimum_action_count integer not null check (minimum_action_count > 0),
  base_reward_points integer not null check (base_reward_points >= 0),
  streak_eligible boolean not null default true,
  is_active boolean not null default true,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.owner_indoor_mission_daily_state (
  owner_user_id uuid not null references auth.users(id) on delete cascade,
  day_key date not null,
  pet_context_id text not null,
  pet_name_snapshot text not null default '강아지',
  age_years integer,
  age_band text not null default 'unknown',
  activity_level text not null default 'moderate',
  walk_frequency text not null default 'steady',
  applied_multiplier double precision not null default 1.0 check (applied_multiplier >= 0.75 and applied_multiplier <= 1.25),
  adjustment_description text not null default '기본 난이도 유지',
  adjustment_reasons jsonb not null default '[]'::jsonb,
  easy_day_applied boolean not null default false,
  easy_day_activated_at timestamptz,
  extension_state text not null default 'none',
  extension_source_day_key date,
  extension_source_instance_id uuid,
  base_risk_level text not null default 'clear',
  effective_risk_level text not null default 'clear',
  refreshed_at timestamptz not null default now(),
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  primary key (owner_user_id, day_key, pet_context_id)
);

create table if not exists public.owner_indoor_mission_instances (
  id uuid primary key default gen_random_uuid(),
  owner_user_id uuid not null references auth.users(id) on delete cascade,
  day_key date not null,
  pet_context_id text not null,
  instance_key text not null unique,
  template_id text not null references public.indoor_mission_templates(id) on delete restrict,
  category text not null,
  title_snapshot text not null,
  description_snapshot text not null,
  minimum_action_count_snapshot integer not null check (minimum_action_count_snapshot > 0),
  reward_points_snapshot integer not null check (reward_points_snapshot >= 0),
  streak_eligible_snapshot boolean not null,
  action_count integer not null default 0 check (action_count >= 0),
  status text not null default 'active',
  is_extension boolean not null default false,
  extension_source_day_key date,
  extension_reward_scale double precision not null default 1.0,
  claimed_at timestamptz,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.owner_indoor_mission_action_events (
  id bigint generated by default as identity primary key,
  owner_user_id uuid not null references auth.users(id) on delete cascade,
  mission_instance_id uuid not null references public.owner_indoor_mission_instances(id) on delete cascade,
  event_id text not null,
  delta_value integer not null default 1 check (delta_value > 0),
  payload jsonb not null default '{}'::jsonb,
  recorded_at timestamptz not null default now(),
  created_at timestamptz not null default now(),
  unique (mission_instance_id, event_id)
);

create table if not exists public.owner_indoor_mission_claims (

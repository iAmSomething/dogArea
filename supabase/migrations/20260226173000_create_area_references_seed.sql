-- Global/public area reference data for comparison cards and milestones.
-- Includes:
-- 1) legacy app dataset from AreaMeters.swift
-- 2) additional curated landmarks with explicit sources

create table if not exists public.area_references (
  id uuid primary key default gen_random_uuid(),
  reference_name text not null unique,
  area_m2 double precision not null check (area_m2 > 0),
  category text not null default 'landmark',
  country_code text,
  source_label text,
  source_url text,
  source_note text,
  is_active boolean not null default true,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists idx_area_references_area_m2 on public.area_references(area_m2);
create index if not exists idx_area_references_category on public.area_references(category);

-- Keep updated_at maintained by the shared trigger function.
drop trigger if exists trg_area_references_set_updated_at on public.area_references;
create trigger trg_area_references_set_updated_at
before update on public.area_references
for each row
execute function public.set_updated_at();

alter table public.area_references enable row level security;

drop policy if exists area_references_select_all on public.area_references;
create policy area_references_select_all
on public.area_references
for select
to anon, authenticated
using (true);

drop policy if exists area_references_manage_service_role on public.area_references;
create policy area_references_manage_service_role
on public.area_references
for all
to service_role
using (true)
with check (true);

grant select on public.area_references to anon, authenticated;
grant all on public.area_references to service_role;

